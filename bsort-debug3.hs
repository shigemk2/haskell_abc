import Debug.Trace

bubble [x] = [x]
bubble (x:xs)
    | y < x     = y:x:ys
    | otherwise = x:y:ys
    where
      (y:ys) = bubble xs

bsort [] = []
bsort xs = y : bsort ys
  where
    (y:ys) = bubble xs

